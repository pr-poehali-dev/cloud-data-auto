function BlockRenderer({ block }: { block: Block }) {
  if (block.code) {
    return (
      <pre style={{ background: "#f5f2ee", border: "1px solid #e0d9d0", borderLeft: "3px solid #c8a96e", borderRadius: "2px", padding: "14px 16px", fontSize: "11px", lineHeight: "1.7", fontFamily: "'IBM Plex Mono', monospace", color: "#2a2420", overflowX: "auto", marginBottom: "14px", whiteSpace: "pre-wrap" }}>
        {block.code}
      </pre>
    );
  }

  if (block.table) {
    return (
      <table style={{ width: "100%", borderCollapse: "collapse", fontSize: "11px", marginBottom: "14px", marginTop: "6px" }}>
        <thead>
          <tr>
            {block.table.headers.map(h => (
              <th key={h} style={{ background: "#f0ede8", border: "1px solid #d5cfc8", padding: "6px 8px", textAlign: "left", fontWeight: "700", color: "#3d3530", fontSize: "10px", textTransform: "uppercase", letterSpacing: "0.04em" }}>{h}</th>
            ))}
          </tr>
        </thead>
        <tbody>
          {block.table.rows.map((row, i) => (
            <tr key={i} style={{ background: i % 2 === 0 ? "white" : "#faf8f5" }}>
              {row.map((cell, j) => (
                <td key={j} style={{ border: "1px solid #e8e0d4", padding: "6px 8px", verticalAlign: "top", lineHeight: "1.6" }}>{cell}</td>
              ))}
            </tr>
          ))}
        </tbody>
      </table>
    );
  }

  if (block.list && !block.bold) {
    return (
      <div style={{ marginBottom: "10px", paddingLeft: "4px" }}>
        {block.list.map((item, i) => (
          <div key={i} style={{ fontSize: "13px", lineHeight: "1.8", display: "flex", gap: "8px", marginBottom: "2px" }}>
            <span style={{ color: "#888", flexShrink: 0, minWidth: "20px" }}>{i + 1}.</span>
            <span style={{ textAlign: "justify" }}>{item}</span>
          </div>
        ))}
      </div>
    );
  }

  if (block.bold && block.list) {
    return (
      <div style={{ marginBottom: "10px" }}>
        <p style={{ fontSize: "13px", lineHeight: "1.8", marginBottom: "4px" }}><strong>{block.bold}</strong></p>
        <div style={{ paddingLeft: "4px" }}>
          {block.list.map((item, i) => (
            <div key={i} style={{ fontSize: "13px", lineHeight: "1.8", display: "flex", gap: "8px", marginBottom: "2px" }}>
              <span style={{ color: "#888", flexShrink: 0, minWidth: "20px" }}>{i + 1}.</span>
              <span style={{ textAlign: "justify" }}>{item}</span>
            </div>
          ))}
        </div>
      </div>
    );
  }

  return (
    <p style={{ fontSize: "13px", lineHeight: "1.8", marginBottom: "10px", textAlign: "justify" }}>
      {block.bold && <strong>{block.bold}</strong>}
      {block.text}
    </p>
  );
}
