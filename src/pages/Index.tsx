type Block = {
  text?: string;
  bold?: string;
  list?: string[];
  table?: { headers: string[]; rows: string[][] };
  code?: string;
};

type Section = {
  page?: boolean;
  type?: string;
  heading?: string;
  sub?: string;
  blocks?: Block[];
};

const PLAIN = DOC.flatMap(s => {
  const lines: string[] = [];
  if (s.heading) lines.push("\n" + s.heading + "\n");
  if (s.sub) lines.push(s.sub + "\n");
  s.blocks?.forEach(b => {
    if (b.bold && b.text) lines.push(b.bold + b.text);
    else if (b.bold && b.list) { lines.push(b.bold); b.list.forEach((l, i) => lines.push(`${i + 1}. ${l}`)); }
    else if (b.text) lines.push(b.text);
    if (b.list && !b.bold) b.list.forEach((l, i) => lines.push(`${i + 1}. ${l}`));
    if (b.code) lines.push(b.code);
    if (b.table) { lines.push(b.table.headers.join(" | ")); b.table.rows.forEach(r => lines.push(r.join(" | "))); }
  });
  return lines;
}).join("\n\n");

